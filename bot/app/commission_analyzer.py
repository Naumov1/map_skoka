def should_analyze_text(text: str) -> bool:
    lowered = text.lower()
    if lowered.startswith(("/itog", "/итог", "/analysis", "/analyze", "/анализ")):
        return True
    if lowered.startswith(("итог:", "анализ:", "разбор:")):
        return True
    markers = ("заявление", "жалоба", "прошу", "затоп", "фасад", "горяч", "крыша", "лифт", "освещ", "ламп", "стояк")
    return len(text) > 120 and any(marker in lowered for marker in markers)


def strip_analysis_command(text: str) -> str:
    stripped = text.strip()
    lowered = stripped.lower()
    for command in ("/itog", "/итог", "/analysis", "/analyze", "/анализ"):
        if lowered.startswith(command):
            return stripped[len(command):].strip(" :\n")
    for prefix in ("итог:", "анализ:", "разбор:"):
        if lowered.startswith(prefix):
            return stripped[len(prefix):].strip()
    return stripped


def format_analysis(analysis: CommissionAnalysis) -> str:
    def lines(items: list[str]) -> str:
        return "\n".join(f"- {item}" for item in items)

    return (
        "Итог для комиссии\n\n"
        f"Категория: {analysis.category}\n"
        f"Срочность: {analysis.urgency}\n"
        f"Заявитель: {analysis.applicant}\n"
        f"Адрес: {analysis.address}\n\n"
        f"Что случилось:\n{analysis.what_happened}\n\n"
        f"Кто нужен:\n{lines(analysis.who_needed)}\n\n"
        f"Как действовать:\n{lines(analysis.recommended_actions)}\n\n"
        f"Фокус комиссии:\n{lines(analysis.commission_focus)}\n\n"
        f"Риски:\n{lines(analysis.risks)}"
    )

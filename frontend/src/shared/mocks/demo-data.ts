const publicVoronezhApplications = demoApplications.filter((application) => Number(application.id) >= 1101);

export function mergeWithPublicVoronezhApplications(applications: Application[]) {
  const addresses = new Set(applications.map((application) => (application.address || "").toLowerCase()));
  return [
    ...applications,
    ...publicVoronezhApplications.filter((application) => !addresses.has((application.address || "").toLowerCase()))
  ];
}

export function mergeRiskMapApplications(...groups: Application[][]) {
  const seen = new Set<string>();
  return groups.flat().filter((application) => {
    const key = `${application.id || ""}:${(application.address || "").toLowerCase().replace(/\s+/g, " ").trim()}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

export async function fetchApplicationsWithoutRedirect() {
  const response = await fetch(`${API_BASE}/applications/risk-map`, {
    credentials: "include",
    headers: { accept: "application/json" }
  });
  if (!response.ok) return [];
  const data = await response.json().catch(() => ({})) as { applications?: Application[] };
  return Array.isArray(data.applications) ? data.applications : [];
}



